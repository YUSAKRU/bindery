const STORAGE_KEY = 'bindery.lang';
let currentLang: Lang = 'en';

export function getLanguage(): Lang {
  return currentLang;
}

export function t(key: string, params?: Record<string, string | number>): string {
  let str = STRINGS[currentLang][key] ?? STRINGS.en[key] ?? key;
  if (params) {
    for (const [name, value] of Object.entries(params)) {
      str = str.replace(`{${name}}`, String(value));
    }
  }
  return str;
}

export function applyLanguageToDom(): void {
  document.documentElement.lang = currentLang;
  document.querySelectorAll<HTMLElement>('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (key) el.textContent = t(key);
  });
  document.querySelectorAll<HTMLInputElement>('[data-i18n-placeholder]').forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (key) el.placeholder = t(key);
  });
  document.querySelectorAll<HTMLElement>('[data-i18n-aria-label]').forEach((el) => {
    const key = el.dataset.i18nAriaLabel;
    if (key) el.setAttribute('aria-label', t(key));
  });
}

export function setLanguage(lang: Lang): void {
  currentLang = lang;
  localStorage.setItem(STORAGE_KEY, lang);
  applyLanguageToDom();
}

export function initLanguage(): void {
  currentLang = localStorage.getItem(STORAGE_KEY) === 'tr' ? 'tr' : 'en';
  applyLanguageToDom();
}
